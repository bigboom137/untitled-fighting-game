info.onCountdownEnd(function () {
    if (info.player2.life() > info.player1.life()) {
        mp.gameOverPlayerWin(mp.playerSelector(mp.PlayerNumber.Two))
    } else if (info.player2.life() < info.player1.life()) {
        mp.gameOverPlayerWin(mp.playerSelector(mp.PlayerNumber.One))
    } else {
        game.showLongText("OVERTIME!", DialogLayout.Bottom)
        overtime += 1
    }
})
sprites.onOverlap(SpriteKind.Food, SpriteKind.Player, function (sprite, otherSprite) {
    pause(200)
    if (activatePowerup > 0) {
        if (otherSprite == P1) {
            P2MoveDisabled += 1
            activatePowerup = 0
        }
        if (otherSprite == P2) {
            P1MoveDisabled += 1
            activatePowerup = 0
        }
    }
    if (activatePowerup2 > 0) {
        if (otherSprite == P1) {
            P1Duck = 1
            activatePowerup2 = 0
        }
        if (otherSprite == P2) {
            P2Duck = 1
            activatePowerup2 = 0
        }
    }
    if (ActivatePowerup3 > 0) {
        if (otherSprite == P1) {
            P1MoveBoost = 1
            ActivatePowerup3 = 0
        }
        if (otherSprite == P2) {
            P2MoveBoost = 1
            ActivatePowerup3 = 0
        }
    }
    if (ActivatePowerup4 > 0) {
        if (otherSprite == P1) {
            info.player1.changeLifeBy(1)
            ActivatePowerup4 = 0
        }
        if (otherSprite == P2) {
            info.player2.changeLifeBy(1)
            ActivatePowerup4 = 0
        }
    }
})
